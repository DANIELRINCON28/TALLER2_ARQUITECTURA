def demo_adapter_pattern():
    """Demuestra el patr√≥n Adapter para unificar APIs de proveedores."""
    print_section("PATR√ìN ESTRUCTURAL: ADAPTER")
    
    print("El patr√≥n Adapter permite que interfaces incompatibles trabajen juntas.")
    print("Unifica las diferentes APIs de proveedores bajo una interfaz com√∫n.\n")
    
    # Demostrar adapters para diferentes proveedores
    providers = ['motoya', 'ecobike', 'paqz']
    order_data = {
        'order_id': 123,
        'weight': 1500,
        'priority': 'normal',
        'fragility': 'debil',
        'address': 'Calle Principal 456'
    }
    
    print("üîå Probando adapters con datos unificados:")
    print(f"   üì¶ Datos del pedido: {order_data}\n")
    
    for provider_name in providers:
        try:
            # Crear adapter usando factory
            adapter = ShippingAdapterFactory.create_adapter(provider_name)
            
            # Usar interfaz unificada
            tracking_id = adapter.request_pickup(order_data)
            
            print(f"‚úÖ {adapter.get_provider_name().upper()}")
            print(f"   üè∑Ô∏è  Tracking generado: {tracking_id}")
            print(f"   üîß API espec√≠fica adaptada correctamente")
            
        except Exception as e:
            print(f"‚ùå Error con {provider_name}: {e}")
    
    print(f"\nüí° VENTAJAS del patr√≥n Adapter:")
    print(f"   - Interfaz unificada para APIs heterog√©neas")
    print(f"   - F√°cil agregar nuevos proveedores")
    print(f"   - Desacopla cliente de implementaciones espec√≠ficas")
    print(f"   - Factory pattern para gesti√≥n centralizada")


def demo_strategy_pattern():
    """Demuestra el patr√≥n Strategy para selecci√≥n de proveedores."""
    print_section("PATR√ìN COMPORTAMENTAL: STRATEGY")
    
    print("El patr√≥n Strategy define una familia de algoritmos intercambiables.")
    print("Permite cambiar el algoritmo de selecci√≥n de proveedores din√°micamente.\n")
    
    # Datos de prueba para diferentes escenarios
    test_scenarios = [
        {
            'name': 'Pedido Ligero Normal',
            'data': {'weight': 800, 'priority': 'normal', 'fragility': 'ninguna'}
        },
        {
            'name': 'Pedido Express Fr√°gil',
            'data': {'weight': 1500, 'priority': 'express', 'fragility': 'alta'}
        },
        {
            'name': 'Pedido Pesado',
            'data': {'weight': 4000, 'priority': 'normal', 'fragility': 'debil'}
        }
    ]
    
    # Estrategias disponibles
    strategies = {
        'Est√°ndar': StandardSelectionStrategy(),
        'Ecol√≥gica': EcoFriendlySelectionStrategy(),
        'Optimizada por Costo': CostOptimizedSelectionStrategy()
    }
    
    for scenario in test_scenarios:
        print(f"üìä Escenario: {scenario['name']}")
        print(f"   Datos: {scenario['data']}")
        
        selector = ProviderSelector()
        
        for strategy_name, strategy in strategies.items():
            # Cambiar estrategia din√°micamente
            selector.set_strategy(strategy)
            result = selector.select_provider(scenario['data'])
            
            print(f"   üéØ {strategy_name}: {result['provider']} - {result['reason']}")
        
        print()
    
    print(f"üí° VENTAJAS del patr√≥n Strategy:")
    print(f"   - Algoritmos intercambiables en tiempo de ejecuci√≥n")
    print(f"   - Elimina condicionales complejas (if/else)")
    print(f"   - F√°cil agregar nuevas estrategias")
    print(f"   - Principio Abierto/Cerrado (OCP)")


def demo_observer_pattern():
    """Demuestra el patr√≥n Observer para notificaciones."""
    print_section("PATR√ìN COMPORTAMENTAL: OBSERVER")
    
    print("El patr√≥n Observer define dependencia uno-a-muchos entre objetos.")
    print("Permite notificar autom√°ticamente a m√∫ltiples canales cuando cambia el estado.\n")
    
    # Crear sujeto observable
    notification_subject = OrderNotificationSubject()
    
    # Crear observadores (canales de notificaci√≥n)
    email_observer = EmailNotificationObserver()
    webhook_observer = WebhookNotificationObserver("https://api.sistema-externo.com/hooks")
    sms_observer = SMSNotificationObserver("+57-300-123-4567")
    
    print("üîî Configurando observadores:")
    
    # Agregar observadores din√°micamente
    notification_subject.attach_observer(email_observer)
    notification_subject.attach_observer(webhook_observer)
    notification_subject.attach_observer(sms_observer)
    
    print(f"   üìù Observadores activos: {notification_subject.get_observers_info()}")
    
    # Crear pedido temporal para demostraci√≥n (sin persistir para evitar conflictos FK)
    from mercado_barrio.orders.models import Order
    
    # Intentar usar pedido existente, o crear uno temporal sin guardar
    try:
        # Usar el √∫ltimo pedido de la base de datos si existe
        mock_order = Order.objects.last()
        if not mock_order:
            # Si no hay pedidos, crear uno temporal sin guardar
            mock_order = Order(
                id=9999,  # ID alto para evitar conflictos
                customer_email="demo@mercadobarrio.com",
                address="Direcci√≥n Demo 123",
                priority="express",
                fragility="alta",
                total_weight=1200
            )
            # Guardar temporalmente para que funcionen las notificaciones
            mock_order.save()
            temp_order_created = True
        else:
            temp_order_created = False
            print(f"   üì¶ Usando pedido existente #{mock_order.id} para demostraci√≥n")
    except Exception as e:
        print(f"   ‚ö†Ô∏è  Error accediendo a pedidos existentes: {e}")
        # Crear pedido temporal
        mock_order = Order(
            id=9999,
            customer_email="demo@mercadobarrio.com", 
            address="Direcci√≥n Demo 123",
            priority="express",
            fragility="alta",
            total_weight=1200
        )
        mock_order.save()
        temp_order_created = True
    
    print(f"\nüì¶ Simulando eventos de pedido #{mock_order.id}:")
    
    # Simular diferentes eventos del ciclo de vida del pedido
    events = [
        ('CREATED', 'Pedido confirmado y asignado a EcoBike'),
        ('DISPATCHED', 'Pedido empacado y listo para recogida'),
        ('IN_TRANSIT', 'Pedido en camino al destino'),
        ('DELIVERED', 'Pedido entregado exitosamente')
    ]
    
    for event_type, message in events:
        print(f"\n   üîÑ Evento: {event_type}")
        notification_subject.notify_observers(mock_order, event_type, message)
    
    # Mostrar historial
    print(f"\nüìä Historial de notificaciones:")
    history = notification_subject.get_notification_history()
    for i, event in enumerate(history[-2:], 1):  # Mostrar √∫ltimos 2
        print(f"   {i}. {event['event_type']} - {event['observers_count']} canales notificados")
    
    # Limpiar pedido temporal si fue creado
    if temp_order_created:
        try:
            mock_order.delete()
            print(f"   üßπ Pedido temporal #{mock_order.id} eliminado")
        except:
            pass
    
    print(f"\nüí° VENTAJAS del patr√≥n Observer:")
    print(f"   - Desacoplamiento entre sujeto y observadores")
    print(f"   - Agregar/remover observadores din√°micamente")
    print(f"   - Notificaci√≥n autom√°tica a m√∫ltiples canales")
    print(f"   - Extensible para nuevos tipos de notificaciones")


def demo_integrated_workflow():
    """Demuestra el flujo completo con todos los patrones integrados."""
    print_section("DEMOSTRACI√ìN INTEGRADA - TODOS LOS PATRONES")
    
    print("Flujo completo de creaci√≥n de pedido usando todos los patrones juntos.\n")
    
    # Verificar productos disponibles
    products = products_all()
    if not products:
        print("‚ùå No hay productos en la base de datos.")
        print("üí° Ejecuta el script de carga de datos primero: python manage.py loaddata seed_data.json")
        return
    
    print(f"üì¶ Productos disponibles: {len(products)}")
    for product in products[:3]:  # Mostrar primeros 3
        print(f"   {product.id}. {product.name} ({product.weight_grams}g)")
    
    # Datos de pedido de ejemplo
    order_data = {
        'customer_email': 'cliente.demo@mercadobarrio.com',
        'address': 'Carrera 15 #93-47, Bogot√°, Colombia',
        'priority': 'express',
        'fragility': 'alta',
        'items': {str(products[0].id): 2, str(products[1].id): 1} if len(products) >= 2 else {str(products[0].id): 1}
    }
    
    print(f"\nüìù Datos del pedido:")
    for key, value in order_data.items():
        print(f"   {key}: {value}")
    
    print(f"\nüöÄ Ejecutando creaci√≥n de pedido con TODOS los patrones...")
    print(f"   (Ver logs detallados del proceso)\n")
    
    try:
        # Probar diferentes estrategias
        strategies = ['standard', 'eco', 'cost']
        
        for strategy in strategies:
            print(f"\n" + "="*50)
            print(f"üéØ PROBANDO ESTRATEGIA: {strategy.upper()}")
            print(f"="*50)
            
            # Crear pedido con la estrategia espec√≠fica
            order_id = handle_create_order(order_data, strategy_type=strategy)
            
            print(f"\n‚úÖ Pedido #{order_id} creado exitosamente con estrategia {strategy}")
            
    except Exception as e:
        print(f"‚ùå Error en flujo integrado: {e}")
